public class Java100_operator_ArithmeticOperators2 {
    public static void main(String[]args){

        int a=0, b=100;
        a=a+1;
        System.out.println(a);

        //축약된 형태
        a+=1; //a=a+1을 축약한 형태
        System.out.println(a);

        a-=1; //a=a-1을 축약한 형태
        System.out.println(a);

        b++; //b=b+1을 축약한 형태
        System.out.println(b);

        b--; //b=b-1을 축약한 형태
        System.out.println(b);
    }
}
